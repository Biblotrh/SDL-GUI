namespace SDLGUI
{
    public class Colour
    {
        public byte r = 255;
        public byte g = 255;
        public byte b = 255;
        public byte a = 255;
        public Colour(byte r = 255, byte g = 255, byte b = 255, byte a = 255)
        {
            this.r = r;
            this.g = g;
            this.b = b;
            this.a = a;
        }
    }
}
